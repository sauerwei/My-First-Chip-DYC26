module arrow_bitmap(
    input  logic [5:0] y_offset,    // row in map (0-41)
    input  logic [5:0] x_offset,    // pixel in row (0-41)
    input  logic [3:0] requested_dir, // 4-bit direction code
    output logic       is_arrow     // 1 = on, 0 = off
);
    logic [47:0] row_data;
    logic [47:0] arrow_rom [3:0][47:0]; 
    logic [1:0] dir_idx;
    logic       dir_valid;

    initial begin
        integer i;
        integer j;
        for (i = 0; i < 4; i = i + 1)
            for (j = 0; j < 48; j = j + 1)
                arrow_rom[i][j] = 48'b0;

        // UP
        arrow_rom[0][0] = 48'b000000000000000000000001100000000000000000000000;
        arrow_rom[0][1] = 48'b000000000000000000000001100000000000000000000000;
        arrow_rom[0][2] = 48'b000000000000000000001111111100000000000000000000;
        arrow_rom[0][3] = 48'b000000000000000000001111111100000000000000000000;
        arrow_rom[0][4] = 48'b000000000000000000111111111111000000000000000000;
        arrow_rom[0][5] = 48'b000000000000000000111111111111000000000000000000;

        // DOWN
        arrow_rom[1][42] = 48'b000000000000000001111111111110000000000000000000;
        arrow_rom[1][43] = 48'b000000000000000001111111111110000000000000000000;
        arrow_rom[1][44] = 48'b000000000000000000011111111000000000000000000000;
        arrow_rom[1][45] = 48'b000000000000000000011111111000000000000000000000;
        arrow_rom[1][46] = 48'b000000000000000000000011000000000000000000000000;
        arrow_rom[1][47] = 48'b000000000000000000000011000000000000000000000000;

        // LEFT
        arrow_rom[2][19] = 48'b000000000000000000000000000000000000000000110000;
        arrow_rom[2][20] = 48'b000000000000000000000000000000000000000000110000;
        arrow_rom[2][21] = 48'b000000000000000000000000000000000000000000111100;
        arrow_rom[2][22] = 48'b000000000000000000000000000000000000000000111100;
        arrow_rom[2][23] = 48'b000000000000000000000000000000000000000000111111;
        arrow_rom[2][24] = 48'b000000000000000000000000000000000000000000111111;
        arrow_rom[2][25] = 48'b000000000000000000000000000000000000000000111100;
        arrow_rom[2][26] = 48'b000000000000000000000000000000000000000000111100;
        arrow_rom[2][27] = 48'b000000000000000000000000000000000000000000110000;
        arrow_rom[2][28] = 48'b000000000000000000000000000000000000000000110000;

        // RIGHT
        arrow_rom[3][19] = 48'b000011000000000000000000000000000000000000000000;
        arrow_rom[3][20] = 48'b000011000000000000000000000000000000000000000000;
        arrow_rom[3][21] = 48'b001111000000000000000000000000000000000000000000;
        arrow_rom[3][22] = 48'b001111000000000000000000000000000000000000000000;
        arrow_rom[3][23] = 48'b111111000000000000000000000000000000000000000000;
        arrow_rom[3][24] = 48'b111111000000000000000000000000000000000000000000;
        arrow_rom[3][25] = 48'b001111000000000000000000000000000000000000000000;
        arrow_rom[3][26] = 48'b001111000000000000000000000000000000000000000000;
        arrow_rom[3][27] = 48'b000011000000000000000000000000000000000000000000;
        arrow_rom[3][28] = 48'b000011000000000000000000000000000000000000000000;
    end

    always_comb begin
        dir_idx   = 2'd0;
        dir_valid = 1'b1;

        case (requested_dir)
            4'b0001: dir_idx = 2'd0;
            4'b0010: dir_idx = 2'd1;
            4'b0100: dir_idx = 2'd2;
            4'b1000: dir_idx = 2'd3;
            default: dir_valid = 1'b0;
        endcase

        row_data = dir_valid ? arrow_rom[dir_idx][y_offset] : 48'b0;
    end

    assign is_arrow = row_data[x_offset];

endmodule